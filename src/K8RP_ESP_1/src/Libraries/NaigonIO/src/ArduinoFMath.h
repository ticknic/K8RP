#ifndef __ArduinoFMath_h_
#define __ArduinoFMath_h_

float mapf(float x, float in_min, float in_max, float out_min, float out_max)
{
  return (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
}

#endif //__ArduinoHelpers_h_
